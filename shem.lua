function pshem(pos, shem, asian)
	if(shem == "shem_gate_min")then
		local dir = 0
	local peekaboo = {math.abs(asian.x),math.abs(asian.z)}
	local verdict = math.max(peekaboo[1],peekaboo[2])
	if(verdict == peekaboo[1])then
		dir = 90
	else
		end
